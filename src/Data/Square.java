package Data;

public abstract class Square {

    private static int i;
    private static int j;

    public static int getI() {
        return i;
    }

    public static void setI(int i) {
        Square.i = i;
    }

    public static int getJ() {
        return j;
    }

    public static void setJ(int j) {
        Square.j = j;
    }

}
